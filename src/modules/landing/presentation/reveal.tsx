"use client";

import { motion } from "framer-motion";

/**
 * Pembungkus animasi masuk seksi (fade + slide ringan) saat masuk viewport.
 * Sengaja halus & sekali jalan (viewport once) agar tidak mengganggu.
 */
export function Reveal({
  children,
  delay = 0,
  className,
}: {
  children: React.ReactNode;
  delay?: number;
  className?: string;
}) {
  return (
    <motion.div
      className={className}
      initial={{ opacity: 0, y: 24 }}
      whileInView={{ opacity: 1, y: 0 }}
      viewport={{ once: true, margin: "-80px" }}
      transition={{ duration: 0.5, delay, ease: [0.22, 1, 0.36, 1] }}
    >
      {children}
    </motion.div>
  );
}
